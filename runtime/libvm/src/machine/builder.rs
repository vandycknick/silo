#[derive(Debug, Clone)]
struct MachineCreateRequest {
    image_source: Option<ImageSource>,
    name: Option<String>,
    labels: BTreeMap<String, String>,
    metadata: BTreeMap<String, String>,
    cpus: Option<u8>,
    memory: Option<Memory>,
    kernel: Option<PathBuf>,
    initramfs: Option<PathBuf>,
    disk_size_bytes: Option<u64>,
    nested_virtualization: bool,
    rosetta: bool,
    userdata: Option<String>,
    disks: Vec<PathBuf>,
    mounts: Vec<Mount>,
    network: Option<MachineNetworkConfig>,
    network_error: Option<String>,
    guest: MachineGuestConfig,
}

struct MachineCreatePlan {
    name: String,
    spec: VmSpec,
    labels: BTreeMap<String, String>,
    metadata: BTreeMap<String, String>,
    network: ModelMachineNetworkConfig,
    guest: MachineGuestConfig,
}

struct MachineCreateGuard {
    id: MachineId,
    name: String,
    spec: VmSpec,
    machine_dir: PathBuf,
    lock: Option<ManagedLock>,
    labels: BTreeMap<String, String>,
    metadata: BTreeMap<String, String>,
    network: ModelMachineNetworkConfig,
    guest: MachineGuestConfig,
    dir_created: bool,
    committed: bool,
}

/// Builder for creating a machine.
///
/// The builder records durable desired configuration. It does not start vmmon or
/// prepare networking. Those runtime artifacts are regenerated by `Machine::start`.
/// Image materialization happens during `create`; `Machine::start` only boots the
/// already-created root disk.
///
/// ```rust,no_run
/// use libvm::{ImageSource, Memory, Runtime};
///
/// # async fn example(runtime: Runtime) -> Result<(), libvm::LibVmError> {
/// let machine = runtime
///     .machine()
///     .image("docker.io/library/alpine:latest")
///     .name("devbox")
///     .cpus(4)
///     .memory(Memory::gibibytes(8))
///     .network(|network| network.private())
///     .create()
///     .await?;
/// let from_disk = runtime
///     .machine()
///     .image_source(ImageSource::disk("/tmp/rootfs.img"))
///     .create()
///     .await?;
/// # let _ = from_disk;
/// # let _ = machine;
/// # Ok(())
/// # }
/// ```
pub struct MachineBuilder {
    runtime: Runtime,
    request: MachineCreateRequest,
}

impl MachineBuilder {
    pub(crate) fn new(runtime: Runtime) -> Self {
        Self {
            runtime,
            request: MachineCreateRequest {
                image_source: None,
                name: None,
                labels: BTreeMap::new(),
                metadata: BTreeMap::new(),
                cpus: None,
                memory: None,
                kernel: None,
                initramfs: None,
                disk_size_bytes: None,
                nested_virtualization: false,
                rosetta: false,
                userdata: None,
                disks: Vec::new(),
                mounts: Vec::new(),
                network: None,
                network_error: None,
                guest: MachineGuestConfig::default(),
            },
        }
    }

    /// Selects an OCI image reference for this machine.
    ///
    /// The string is always interpreted as OCI. Use `image_source` or
    /// `image_with` for local disk and tar sources.
    pub fn image(mut self, reference: impl Into<String>) -> Self {
        self.request.image_source = Some(ImageSource::oci(reference));
        self
    }

    /// Selects an explicit image source.
    pub fn image_source(mut self, source: ImageSource) -> Self {
        self.request.image_source = Some(source);
        self
    }

    /// Selects an image source using a dedicated image builder.
    pub fn image_with(mut self, configure: impl FnOnce(ImageBuilder) -> ImageBuilder) -> Self {
        self.request.image_source = configure(ImageBuilder::new()).finish();
        self
    }

    /// Sets an explicit machine name.
    pub fn name(mut self, name: impl Into<String>) -> Self {
        self.request.name = Some(name.into());
        self
    }

    /// Replaces all labels.
    pub fn labels(mut self, labels: BTreeMap<String, String>) -> Self {
        self.request.labels = labels;
        self
    }

    /// Adds one label.
    pub fn label(mut self, key: impl Into<String>, value: impl Into<String>) -> Self {
        self.request.labels.insert(key.into(), value.into());
        self
    }

    /// Replaces all metadata.
    pub fn metadata(mut self, metadata: BTreeMap<String, String>) -> Self {
        self.request.metadata = metadata;
        self
    }

    /// Adds one metadata entry.
    pub fn metadata_entry(mut self, key: impl Into<String>, value: impl Into<String>) -> Self {
        self.request.metadata.insert(key.into(), value.into());
        self
    }

    /// Sets the virtual CPU count.
    pub fn cpus(mut self, cpus: u8) -> Self {
        self.request.cpus = Some(cpus);
        self
    }

    /// Sets the machine memory.
    pub fn memory(mut self, memory: Memory) -> Self {
        self.request.memory = Some(memory);
        self
    }

    /// Sets a kernel path override.
    pub fn kernel(mut self, kernel: impl Into<PathBuf>) -> Self {
        self.request.kernel = Some(kernel.into());
        self
    }

    /// Sets an initramfs path override.
    pub fn initramfs(mut self, initramfs: impl Into<PathBuf>) -> Self {
        self.request.initramfs = Some(initramfs.into());
        self
    }

    /// Configures durable guest behavior outside the VMM specification.
    pub fn guest(mut self, configure: impl FnOnce(GuestBuilder) -> GuestBuilder) -> Self {
        self.request.guest = configure(GuestBuilder::new()).build();
        self
    }

    /// Sets the desired root disk size in bytes.
    pub fn root_disk_size(mut self, bytes: u64) -> Self {
        self.request.disk_size_bytes = Some(bytes);
        self
    }

    /// Enables or disables nested virtualization.
    pub fn nested_virtualization(mut self, enabled: bool) -> Self {
        self.request.nested_virtualization = enabled;
        self
    }

    /// Enables or disables Rosetta support.
    pub fn rosetta(mut self, enabled: bool) -> Self {
        self.request.rosetta = enabled;
        self
    }

    /// Sets guest userdata.
    pub fn userdata(mut self, userdata: impl Into<String>) -> Self {
        self.request.userdata = Some(userdata.into());
        self
    }

    /// Replaces additional disk paths.
    pub fn disks(mut self, disks: Vec<PathBuf>) -> Self {
        self.request.disks = disks;
        self
    }

    /// Replaces guest mounts.
    pub fn mounts(mut self, mounts: Vec<Mount>) -> Self {
        self.request.mounts = mounts;
        self
    }

    /// Configures the durable network attachment.
    pub fn network(
        mut self,
        configure: impl FnOnce(MachineNetworkBuilder) -> MachineNetworkBuilder,
    ) -> Self {
        match configure(MachineNetworkBuilder::new()).build() {
            Ok(network) => self.request.network = Some(network),
            Err(reason) => self.request.network_error = Some(reason),
        }
        self
    }

    /// Creates the machine.
    pub async fn create(self) -> Result<Machine, LibVmError> {
        let runtime = self.runtime;
        let config = create_machine_config(&runtime, self.request).await?;
        Ok(Machine::new(runtime.without_image_progress(), config.id))
    }
}

async fn create_machine_config(
    runtime: &Runtime,
    request: MachineCreateRequest,
) -> Result<MachineConfig, LibVmError> {
    let Some(name) = request.name.clone() else {
        return create_machine_config_with_generated_name(runtime, request).await;
    };

    create_machine_config_with_name(runtime, request, name).await
}

async fn create_machine_config_with_generated_name(
    runtime: &Runtime,
    request: MachineCreateRequest,
) -> Result<MachineConfig, LibVmError> {
    for _ in 0..GENERATED_NAME_ATTEMPTS {
        let name = generate_machine_name()?;
        match create_machine_config_with_name(runtime, request.clone(), name).await {
            Err(LibVmError::MachineAlreadyExists { .. }) => {}
            result => return result,
        }
    }

    Err(LibVmError::MachineNameGenerationFailed {
        attempts: GENERATED_NAME_ATTEMPTS,
    })
}

async fn create_machine_config_with_name(
    runtime: &Runtime,
    mut request: MachineCreateRequest,
    name: String,
) -> Result<MachineConfig, LibVmError> {
    if matches!(request.disk_size_bytes, Some(0)) {
        return Err(LibVmError::InvalidCreateRequest {
            name,
            reason: "root disk size must be greater than 0".to_string(),
        });
    }
    let Some(image_source) = request.image_source.clone() else {
        return Err(LibVmError::InvalidCreateRequest {
            name,
            reason: "machine image source is required".to_string(),
        });
    };
    if let Some(reason) = request.network_error {
        return Err(LibVmError::InvalidCreateRequest { name, reason });
    }
    if let Some(user) = &request.guest.user {
        user.validate()
            .map_err(|reason| LibVmError::InvalidCreateRequest {
                name: name.clone(),
                reason,
            })?;
    }

    let (kernel, initramfs) = crate::runtime::boot_assets::canonicalize_boot_overrides(
        crate::runtime::boot_assets::BootAssetOverrides {
            kernel: request.kernel.as_deref(),
            initramfs: request.initramfs.as_deref(),
        },
    )?;
    request.guest = crate::runtime::boot_assets::canonicalize_guest_config(request.guest)?;
    if let Some(userdata) = request.userdata.as_deref() {
        if userdata.trim().is_empty() {
            return Err(LibVmError::InvalidCreateRequest {
                name,
                reason: "userdata cannot be empty".to_string(),
            });
        }
    }
    let userdata = request.userdata;
    let disk_paths = canonicalize_existing_paths(&request.disks, "disk")?;

    let resolved_cpus = request.cpus.unwrap_or(DEFAULT_IMAGE_CPUS);
    let resolved_memory = request
        .memory
        .map(|memory| memory.to_vm_spec_mebibytes(&name))
        .transpose()?
        .unwrap_or(DEFAULT_IMAGE_MEMORY_MIB);

    let mounts = assign_mount_tags(request.mounts);
    let disks = std::iter::once(Disk {
        path: root_disk_relative_path(),
        read_only: false,
    })
    .chain(disk_paths.into_iter().map(|path| Disk {
        path,
        read_only: false,
    }))
    .collect();

    let spec = VmSpec {
        guest: Some(Guest {
            os: Some(GuestOs::Linux),
        }),
        boot: Some(Boot {
            kernel: Some(Kernel {
                path: kernel,
                cmdline: vec![ROOT_DISK_KERNEL_ARG.to_string()],
                initramfs,
            }),
            userdata,
        }),
        hardware: Some(Hardware {
            cpus: Some(resolved_cpus),
            memory: Some(resolved_memory),
            nested_virtualization: Some(request.nested_virtualization),
            rosetta: Some(request.rosetta),
        }),
        storage: Some(Storage { disks }),
        mounts,
        ..VmSpec::current()
    };

    let network = request.network.unwrap_or_default().into();
    runtime.validate_machine_network_config(&network).await?;
    let create = create_machine_guard(
        runtime,
        MachineCreatePlan {
            name,
            spec,
            labels: request.labels,
            metadata: request.metadata,
            network,
            guest: request.guest,
        },
    )
    .await?;

    let materialized = runtime.materialize_image(&image_source).await?;
    let root_disk_size = request.disk_size_bytes.unwrap_or(materialized.size_bytes);
    if root_disk_size == 0 {
        return Err(LibVmError::InvalidCreateRequest {
            name: create.name.clone(),
            reason: "root disk size must be greater than 0".to_string(),
        });
    }
    let rootfs_path = MachinePaths::new(create.dir()).root_disk_path();
    clone_or_copy_root_disk(&materialized.rootfs_path, &rootfs_path)?;

    if let Some(size_bytes) = request.disk_size_bytes {
        resize_raw_disk(&rootfs_path, size_bytes)?;
    }

    let rootfs = machine_rootfs_record(&create, &materialized, rootfs_path, root_disk_size);
    create
        .commit(
            runtime,
            rootfs,
            materialized.image_ref,
            Some(root_disk_size),
        )
        .await
}

async fn create_machine_guard(
    runtime: &Runtime,
    plan: MachineCreatePlan,
) -> Result<MachineCreateGuard, LibVmError> {
    let MachineCreatePlan {
        name,
        spec,
        labels,
        metadata,
        network,
        guest,
    } = plan;

    validate_machine_name(&name)?;

    if runtime
        .machine_config_by_name(name.as_str())
        .await?
        .is_some()
    {
        return Err(LibVmError::MachineAlreadyExists { name });
    }

    let id = MachineId::new();
    let machine_dir = runtime.machine_paths(id).dir().to_path_buf();
    let lock = runtime.allocate_machine_lock()?;
    let mut create = MachineCreateGuard {
        id,
        name,
        spec,
        machine_dir,
        lock: Some(lock),
        labels,
        metadata,
        network,
        guest,
        dir_created: false,
        committed: false,
    };

    create.create_machine_dir()?;
    write_machine_config(create.dir(), &create.name, &create.spec)?;

    Ok(create)
}

fn assign_mount_tags(mounts: Vec<Mount>) -> Vec<Mount> {
    mounts
        .into_iter()
        .enumerate()
        .map(|(index, mut mount)| {
            if mount.tag.trim().is_empty() {
                mount.tag = format!("mount{index}");
            }
            mount
        })
        .collect()
}

fn canonicalize_existing_paths(paths: &[PathBuf], kind: &str) -> Result<Vec<PathBuf>, LibVmError> {
    paths
        .iter()
        .map(|path| canonicalize_existing_path(path, kind))
        .collect()
}

fn canonicalize_existing_path(path: &Path, kind: &str) -> Result<PathBuf, LibVmError> {
    let abs = if path.is_absolute() {
        path.to_path_buf()
    } else {
        std::env::current_dir()?.join(path)
    };

    std::fs::canonicalize(&abs).map_err(|err| LibVmError::InvalidCreateRequest {
        name: kind.to_string(),
        reason: format!("{kind} path does not exist: {} ({err})", abs.display()),
    })
}

fn machine_rootfs_record(
    create: &MachineCreateGuard,
    image: &MaterializedImage,
    root_disk_path: PathBuf,
    root_disk_size_bytes: u64,
) -> MachineRootfsRecord {
    MachineRootfsRecord {
        machine_id: create.id,
        source_kind: image.source_kind,
        source_reference: image.source_reference.clone(),
        manifest_digest: image.manifest_digest.clone(),
        image_id: image.image_id.clone(),
        root_disk_path,
        root_disk_size_bytes,
        created_at: now_unix(),
    }
}

impl MachineCreateGuard {
    fn dir(&self) -> &Path {
        &self.machine_dir
    }

    fn create_machine_dir(&mut self) -> Result<(), LibVmError> {
        if let Some(parent) = self.machine_dir.parent() {
            fs::create_dir_all(parent)?;
        }
        let mut builder = fs::DirBuilder::new();
        builder.mode(0o700);
        match builder.create(&self.machine_dir) {
            Ok(()) => {
                self.dir_created = true;
                fs::set_permissions(&self.machine_dir, fs::Permissions::from_mode(0o700))?;
                Ok(())
            }
            Err(err) if err.kind() == std::io::ErrorKind::AlreadyExists => {
                Err(LibVmError::MachineIdAlreadyExists {
                    id: self.id.to_string(),
                })
            }
            Err(err) => Err(err.into()),
        }
    }

    async fn commit(
        mut self,
        runtime: &Runtime,
        rootfs: MachineRootfsRecord,
        image_ref: String,
        root_disk_size: Option<u64>,
    ) -> Result<MachineConfig, LibVmError> {
        let config = self.config(image_ref, root_disk_size);
        let initial_state = stopped_machine_state(self.id, None);
        runtime
            .add_machine_record_with_rootfs(&config, &initial_state, &rootfs)
            .await?;

        self.committed = true;
        Ok(config)
    }

    fn config(&self, image_ref: String, root_disk_size: Option<u64>) -> MachineConfig {
        let now = now_unix();
        MachineConfig {
            id: self.id,
            lock_id: self
                .lock
                .as_ref()
                .expect("machine create guard must hold allocated lock")
                .id(),
            name: self.name.clone(),
            spec: self.spec.clone(),
            machine_dir: self.machine_dir.clone(),
            created_at: now,
            modified_at: now,
            image_ref,
            root_disk_size,
            labels: self.labels.clone(),
            metadata: self.metadata.clone(),
            network: self.network.clone(),
            guest: self.guest.clone(),
        }
    }
}

impl Drop for MachineCreateGuard {
    fn drop(&mut self) {
        if self.committed {
            return;
        }

        if self.dir_created {
            match fs::remove_dir_all(&self.machine_dir) {
                Ok(()) => {}
                Err(err) if err.kind() == std::io::ErrorKind::NotFound => {}
                Err(_) => {}
            }
        }

        if let Some(lock) = self.lock.take() {
            let _ = lock.free();
        }
    }
}

